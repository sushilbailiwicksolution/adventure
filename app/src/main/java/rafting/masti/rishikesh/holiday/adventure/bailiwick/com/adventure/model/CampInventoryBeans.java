public class CampInventoryBeans {

    String Str_id,Str_date,Str_day,Str_total_room,Str_available_room,Str_booked_room;

    public CampInventoryBeans(String str_id, String str_date, String str_day, String str_total_room, String str_available_room, String str_booked_room) {
        Str_id = str_id;
        Str_date = str_date;
        Str_day = str_day;
        Str_total_room = str_total_room;
        Str_available_room = str_available_room;
        Str_booked_room = str_booked_room;
    }

    public String getStr_id() {
        return Str_id;
    }

    public void setStr_id(String str_id) {
        Str_id = str_id;
    }

    public String getStr_date() {
        return Str_date;
    }

    public void setStr_date(String str_date) {
        Str_date = str_date;
    }

    public String getStr_day() {
        return Str_day;
    }

    public void setStr_day(String str_day) {
        Str_day = str_day;
    }

    public String getStr_total_room() {
        return Str_total_room;
    }

    public void setStr_total_room(String str_total_room) {
        Str_total_room = str_total_room;
    }

    public String getStr_available_room() {
        return Str_available_room;
    }

    public void setStr_available_room(String str_available_room) {
        Str_available_room = str_available_room;
    }

    public String getStr_booked_room() {
        return Str_booked_room;
    }

    public void setStr_booked_room(String str_booked_room) {
        Str_booked_room = str_booked_room;
    }
}
