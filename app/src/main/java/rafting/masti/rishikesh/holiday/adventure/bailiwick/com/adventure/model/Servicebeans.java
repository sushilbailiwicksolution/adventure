public class Servicebeans {

    private String Str_id, Str_type, Str_name, Str_sserviceImage;

    public Servicebeans(String str_id, String str_type, String str_name, String str_sserviceImage) {
        Str_id = str_id;
        Str_type = str_type;
        Str_name = str_name;
        Str_sserviceImage = str_sserviceImage;
    }

    public String getStr_id() {
        return Str_id;
    }

    public void setStr_id(String str_id) {
        Str_id = str_id;
    }

    public String getStr_type() {
        return Str_type;
    }

    public void setStr_type(String str_type) {
        Str_type = str_type;
    }

    public String getStr_name() {
        return Str_name;
    }

    public void setStr_name(String str_name) {
        Str_name = str_name;
    }

    public String getStr_sserviceImage() {
        return Str_sserviceImage;
    }

    public void setStr_sserviceImage(String str_sserviceImage) {
        Str_sserviceImage = str_sserviceImage;
    }
}
